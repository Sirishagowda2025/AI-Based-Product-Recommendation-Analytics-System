# ── 4A: Content-Based Filtering ──────────────────────────────
print("\n📌 ENGINE 1: Content-Based Filtering")
print("   Uses product features (category, brand, price, rating)")

le_cat   = LabelEncoder()
le_brand = LabelEncoder()
scaler   = MinMaxScaler()

prod_feat = products.copy()
prod_feat["Category_enc"]    = le_cat.fit_transform(prod_feat["Category"])
prod_feat["Brand_enc"]       = le_brand.fit_transform(prod_feat["Brand"])
prod_feat["Sub_Cat_enc"]     = LabelEncoder().fit_transform(prod_feat["Sub_Category"])

feat_cols = ["Category_enc","Brand_enc","Sub_Cat_enc",
             "Final_Price","Rating_Avg","Discount_%","Review_Count"]
feat_matrix = scaler.fit_transform(prod_feat[feat_cols].fillna(0))
content_sim  = cosine_similarity(feat_matrix)
content_sim_df = pd.DataFrame(content_sim,
                               index=products["Product_ID"],
                               columns=products["Product_ID"])

def content_based_recommend(product_id, n=5):
    """Return top-N similar products based on product features."""
    if product_id not in content_sim_df.index:
        return pd.DataFrame()
    sim_scores = content_sim_df[product_id].drop(product_id).sort_values(ascending=False)
    top_ids    = sim_scores.head(n).index.tolist()
    result     = products[products["Product_ID"].isin(top_ids)].copy()
    result["Similarity_Score"] = result["Product_ID"].map(sim_scores)
    return result[["Product_ID","Product_Name","Category","Brand",
                   "Final_Price","Rating_Avg","Similarity_Score"]].sort_values(
                   "Similarity_Score", ascending=False)

