def hybrid_recommend(user_id, n=5, collab_weight=0.6, content_weight=0.4):
    """
    Blend Collaborative Filtering + Content-Based signals.
    Uses the user's purchase history to anchor content similarity.
    """
    # Collaborative scores
    collab = collab_recommend(user_id, n=n*3)
    if collab.empty: return pd.DataFrame()
    collab_scores = dict(zip(collab["Product_ID"],
                             collab["Predicted_Score"] / (collab["Predicted_Score"].max() + 1e-9)))
    # Content: find most purchased product by user, get similar
    purchased = interact[(interact["User_ID"]==user_id) &
                         (interact["Purchased"]==1)]["Product_ID"].values
    content_scores = {}
    if len(purchased) > 0:
        for pid in purchased[:3]:
            if pid not in content_sim_df.index: continue
            sims = content_sim_df[pid].drop(pid, errors="ignore")
            for k, v in sims.head(n*3).items():
                content_scores[k] = content_scores.get(k, 0) + v
        max_c = max(content_scores.values()) + 1e-9
        content_scores = {k: v/max_c for k, v in content_scores.items()}
    # Blend
    all_pids = set(collab_scores) | set(content_scores)
    seen = set(interact[interact["User_ID"]==user_id]["Product_ID"])
    hybrid = {}
    for pid in all_pids:
        if pid in seen: continue
        c1 = collab_scores.get(pid, 0)
        c2 = content_scores.get(pid, 0)
        hybrid[pid] = collab_weight * c1 + content_weight * c2
    if not hybrid: return pd.DataFrame()
    top_pids = sorted(hybrid, key=hybrid.get, reverse=True)[:n]
    result = products[products["Product_ID"].isin(top_pids)].copy()
    result["Hybrid_Score"] = result["Product_ID"].map(hybrid)
    return result[["Product_ID","Product_Name","Category","Brand",
                   "Final_Price","Rating_Avg","Hybrid_Score"]].sort_values(
                   "Hybrid_Score", ascending=False)

